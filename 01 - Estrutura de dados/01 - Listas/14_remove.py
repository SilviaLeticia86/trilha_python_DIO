# usado para remover o objeto da pilha. O remove irá remover somente a primeira ocorrência. Se em uma lista contiver elementos identicos, os demais não serão removidos.  Podemos chamar o objeto ou o indice
linguagens = ["python", "js", "c", "java", "csharp", "c"]

linguagens.remove("c")

print(linguagens)  # ["python", "js", "java", "csharp"]
